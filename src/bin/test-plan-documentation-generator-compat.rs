#!/usr/bin/env -S cargo +nightly -Zscript
//! Test Plan Documentation Generator Compatibility Checker
//!
//! This binary verifies that container YAML files produced by the verifier
//! are compatible with the test-plan-doc-gen tool's expected input format.
//!
//! Usage:
//!   test-plan-documentation-generator-compat <container.yaml>
//!   test-plan-documentation-generator-compat --batch <directory>
//!   test-plan-documentation-generator-compat --test-verifier-scenarios

use anyhow::{Context, Result};
use clap::{Parser, Subcommand};
use serde::{Deserialize, Serialize};
use serde_yaml::Value;
use std::collections::HashMap;
use std::fs;
use std::path::{Path, PathBuf};

#[derive(Parser, Debug)]
#[command(
    name = "test-plan-documentation-generator-compat",
    about = "Verify container YAML compatibility with test-plan-doc-gen",
    long_about = "This tool validates that container YAML files generated by the verifier \
                  are compatible with the test-plan-doc-gen tool's expected input format. \
                  It can validate individual files, batches of files, or test against \
                  verifier scenario outputs."
)]
struct Cli {
    #[command(subcommand)]
    command: Option<Commands>,

    /// Container YAML file to validate (if no subcommand provided)
    #[arg(value_name = "FILE")]
    file: Option<PathBuf>,

    /// Output detailed validation report
    #[arg(short, long)]
    verbose: bool,

    /// Output report as JSON
    #[arg(long)]
    json: bool,
}

#[derive(Subcommand, Debug)]
enum Commands {
    /// Validate a single container YAML file
    Validate {
        /// Container YAML file to validate
        #[arg(value_name = "FILE")]
        file: PathBuf,

        /// Output detailed validation report
        #[arg(short, long)]
        verbose: bool,

        /// Output report as JSON
        #[arg(long)]
        json: bool,
    },

    /// Validate multiple container YAML files in a directory
    Batch {
        /// Directory containing container YAML files
        #[arg(value_name = "DIR")]
        directory: PathBuf,

        /// Pattern to match files (default: *container*.yaml, *container*.yml)
        #[arg(short, long)]
        pattern: Option<String>,

        /// Recursive search
        #[arg(short, long)]
        recursive: bool,

        /// Continue on errors
        #[arg(short, long)]
        continue_on_error: bool,

        /// Output report as JSON
        #[arg(long)]
        json: bool,
    },

    /// Test against verifier scenario outputs
    TestVerifierScenarios {
        /// Directory containing verifier scenarios (default: testcases/verifier_scenarios)
        #[arg(short, long, default_value = "testcases/verifier_scenarios")]
        scenarios_dir: PathBuf,

        /// Generate container YAML if not found
        #[arg(short, long)]
        generate: bool,
    },

    /// Generate a compatibility report for documentation
    Report {
        /// Directory containing test files
        #[arg(value_name = "DIR")]
        directory: PathBuf,

        /// Output file for the report (default: stdout)
        #[arg(short, long)]
        output: Option<PathBuf>,

        /// Report format (markdown or text)
        #[arg(short, long, default_value = "markdown")]
        format: String,
    },
}

/// Container YAML structure expected by test-plan-doc-gen
#[derive(Debug, Serialize, Deserialize)]
struct ContainerYaml {
    /// Title of the test report
    title: Option<String>,

    /// Project name
    project: Option<String>,

    /// Test date (ISO 8601 format)
    test_date: Option<String>,

    /// Array of test results
    test_results: Vec<TestResult>,

    /// Metadata about the test execution
    metadata: Option<Metadata>,
}

/// Individual test result in the container
#[derive(Debug, Serialize, Deserialize)]
struct TestResult {
    /// Test case ID
    test_case_id: String,

    /// Test case description
    description: Option<String>,

    /// Requirement identifier (optional)
    requirement: Option<String>,

    /// Item number (optional)
    item: Option<u32>,

    /// Test case number (optional)
    tc: Option<u32>,

    /// Test sequences
    sequences: Vec<Sequence>,

    /// Total number of steps
    total_steps: u32,

    /// Number of passed steps
    passed_steps: u32,

    /// Number of failed steps
    failed_steps: u32,

    /// Number of not executed steps
    not_executed_steps: u32,

    /// Overall pass/fail status
    overall_pass: bool,
}

/// Test sequence within a test result
#[derive(Debug, Serialize, Deserialize)]
struct Sequence {
    /// Sequence ID
    sequence_id: u32,

    /// Sequence name
    name: String,

    /// Step results (Pass/Fail/NotExecuted)
    step_results: Vec<Value>,

    /// All steps passed flag
    all_steps_passed: bool,
}

/// Metadata about test execution
#[derive(Debug, Serialize, Deserialize)]
struct Metadata {
    /// Test environment
    environment: Option<String>,

    /// Test platform
    platform: Option<String>,

    /// Test executor
    executor: Option<String>,

    /// Execution duration in seconds
    execution_duration: Option<f64>,

    /// Total test cases
    total_test_cases: Option<u32>,

    /// Passed test cases
    passed_test_cases: Option<u32>,

    /// Failed test cases
    failed_test_cases: Option<u32>,
}

/// Validation result for a container YAML file
#[derive(Debug, Serialize)]
struct ValidationResult {
    /// File path
    file: String,

    /// Is the file valid?
    valid: bool,

    /// Validation errors
    errors: Vec<String>,

    /// Validation warnings
    warnings: Vec<String>,

    /// Schema compatibility issues
    compatibility_issues: Vec<String>,

    /// Additional metadata
    metadata: ValidationMetadata,
}

#[derive(Debug, Serialize)]
struct ValidationMetadata {
    /// Number of test results
    test_results_count: usize,

    /// Total steps across all tests
    total_steps: u32,

    /// Has title field
    has_title: bool,

    /// Has project field
    has_project: bool,

    /// Has test_date field
    has_test_date: bool,

    /// Has metadata section
    has_metadata: bool,
}

/// Compatibility report
#[derive(Debug, Serialize)]
struct CompatibilityReport {
    /// Total files validated
    total_files: usize,

    /// Valid files
    valid_files: usize,

    /// Invalid files
    invalid_files: usize,

    /// Individual validation results
    results: Vec<ValidationResult>,

    /// Summary of compatibility issues
    compatibility_summary: Vec<String>,
}

fn main() -> Result<()> {
    let cli = Cli::parse();

    // Handle legacy usage: test-plan-documentation-generator-compat <file>
    if let Some(file) = &cli.file {
        if cli.command.is_none() {
            let result = validate_container_file(file, cli.verbose)?;

            if cli.json {
                println!("{}", serde_json::to_string_pretty(&result)?);
            } else {
                print_validation_result(&result, cli.verbose);
            }

            if !result.valid {
                std::process::exit(1);
            }

            return Ok(());
        }
    }

    match cli.command {
        Some(Commands::Validate {
            file,
            verbose,
            json,
        }) => {
            let result = validate_container_file(&file, verbose)?;

            if json {
                println!("{}", serde_json::to_string_pretty(&result)?);
            } else {
                print_validation_result(&result, verbose);
            }

            if !result.valid {
                std::process::exit(1);
            }
        }

        Some(Commands::Batch {
            directory,
            pattern,
            recursive,
            continue_on_error,
            json,
        }) => {
            let results = validate_batch(&directory, pattern.as_deref(), recursive)?;

            if json {
                println!("{}", serde_json::to_string_pretty(&results)?);
            } else {
                print_batch_results(&results);
            }

            if !continue_on_error && results.invalid_files > 0 {
                std::process::exit(1);
            }
        }

        Some(Commands::TestVerifierScenarios {
            scenarios_dir,
            generate,
        }) => {
            test_verifier_scenarios(&scenarios_dir, generate)?;
        }

        Some(Commands::Report {
            directory,
            output,
            format,
        }) => {
            generate_compatibility_report(&directory, output.as_deref(), &format)?;
        }

        None => {
            eprintln!("Error: No file or command provided");
            eprintln!("Usage: test-plan-documentation-generator-compat <file>");
            eprintln!("       test-plan-documentation-generator-compat validate <file>");
            eprintln!("       test-plan-documentation-generator-compat --help");
            std::process::exit(1);
        }
    }

    Ok(())
}

fn validate_container_file(file: &Path, verbose: bool) -> Result<ValidationResult> {
    if verbose {
        println!("Validating: {}", file.display());
    }

    let content = fs::read_to_string(file)
        .with_context(|| format!("Failed to read file: {}", file.display()))?;

    let mut errors = Vec::new();
    let mut warnings = Vec::new();
    let mut compatibility_issues = Vec::new();

    // Try to parse as ContainerYaml
    let container: Result<ContainerYaml, _> = serde_yaml::from_str(&content);

    let mut metadata = ValidationMetadata {
        test_results_count: 0,
        total_steps: 0,
        has_title: false,
        has_project: false,
        has_test_date: false,
        has_metadata: false,
    };

    let valid = match container {
        Ok(container) => {
            // Validate required fields
            if container.test_results.is_empty() {
                errors.push("Missing or empty 'test_results' array".to_string());
            }

            // Validate optional but recommended fields
            if container.title.is_none() {
                warnings.push("Missing 'title' field (recommended for reports)".to_string());
            } else {
                metadata.has_title = true;
            }

            if container.project.is_none() {
                warnings.push("Missing 'project' field (recommended for reports)".to_string());
            } else {
                metadata.has_project = true;
            }

            if container.test_date.is_none() {
                warnings.push("Missing 'test_date' field (recommended for reports)".to_string());
            } else {
                metadata.has_test_date = true;
            }

            if container.metadata.is_none() {
                warnings.push("Missing 'metadata' section (recommended for reports)".to_string());
            } else {
                metadata.has_metadata = true;
            }

            // Validate test results structure
            metadata.test_results_count = container.test_results.len();

            for (idx, test_result) in container.test_results.iter().enumerate() {
                metadata.total_steps += test_result.total_steps;

                // Validate required fields in test results
                if test_result.test_case_id.is_empty() {
                    errors.push(format!("Test result #{} has empty test_case_id", idx + 1));
                }

                if test_result.sequences.is_empty() {
                    errors.push(format!(
                        "Test result '{}' has empty sequences array",
                        test_result.test_case_id
                    ));
                }

                // Validate step counts
                let calculated_steps = test_result.passed_steps
                    + test_result.failed_steps
                    + test_result.not_executed_steps;
                if calculated_steps != test_result.total_steps {
                    compatibility_issues.push(format!(
                        "Test result '{}': step count mismatch (passed + failed + not_executed = {} but total_steps = {})",
                        test_result.test_case_id, calculated_steps, test_result.total_steps
                    ));
                }

                // Validate sequences
                for (seq_idx, sequence) in test_result.sequences.iter().enumerate() {
                    if sequence.name.is_empty() {
                        warnings.push(format!(
                            "Test result '{}' sequence #{} has empty name",
                            test_result.test_case_id,
                            seq_idx + 1
                        ));
                    }

                    if sequence.step_results.is_empty() {
                        warnings.push(format!(
                            "Test result '{}' sequence '{}' has empty step_results",
                            test_result.test_case_id, sequence.name
                        ));
                    }

                    // Validate step result variants
                    for (step_idx, step_result) in sequence.step_results.iter().enumerate() {
                        if let Value::Mapping(map) = step_result {
                            let keys: Vec<String> = map
                                .keys()
                                .filter_map(|k| k.as_str().map(|s| s.to_string()))
                                .collect();

                            if keys.is_empty() {
                                errors.push(format!(
                                    "Test result '{}' sequence '{}' step #{} has invalid format",
                                    test_result.test_case_id,
                                    sequence.name,
                                    step_idx + 1
                                ));
                            } else if keys.len() > 1 {
                                compatibility_issues.push(format!(
                                    "Test result '{}' sequence '{}' step #{} has multiple variant keys: {:?}",
                                    test_result.test_case_id,
                                    sequence.name,
                                    step_idx + 1,
                                    keys
                                ));
                            } else {
                                let variant = &keys[0];
                                if !["Pass", "Fail", "NotExecuted"].contains(&variant.as_str()) {
                                    compatibility_issues.push(format!(
                                        "Test result '{}' sequence '{}' step #{} has unknown variant: {}",
                                        test_result.test_case_id,
                                        sequence.name,
                                        step_idx + 1,
                                        variant
                                    ));
                                }

                                // Validate Fail variant has required fields
                                if variant == "Fail" {
                                    if let Some(Value::Mapping(fail_data)) = map.get(variant) {
                                        let has_expected = fail_data
                                            .contains_key(Value::String("expected".to_string()));
                                        let has_actual_result = fail_data.contains_key(
                                            Value::String("actual_result".to_string()),
                                        );
                                        let has_reason = fail_data
                                            .contains_key(Value::String("reason".to_string()));

                                        if !has_expected {
                                            warnings.push(format!(
                                                "Test result '{}' sequence '{}' step #{} Fail variant missing 'expected' field",
                                                test_result.test_case_id,
                                                sequence.name,
                                                step_idx + 1
                                            ));
                                        }
                                        if !has_actual_result {
                                            warnings.push(format!(
                                                "Test result '{}' sequence '{}' step #{} Fail variant missing 'actual_result' field",
                                                test_result.test_case_id,
                                                sequence.name,
                                                step_idx + 1
                                            ));
                                        }
                                        if !has_reason {
                                            warnings.push(format!(
                                                "Test result '{}' sequence '{}' step #{} Fail variant missing 'reason' field",
                                                test_result.test_case_id,
                                                sequence.name,
                                                step_idx + 1
                                            ));
                                        }
                                    }
                                }
                            }
                        } else {
                            errors.push(format!(
                                "Test result '{}' sequence '{}' step #{} is not a mapping",
                                test_result.test_case_id,
                                sequence.name,
                                step_idx + 1
                            ));
                        }
                    }
                }
            }

            errors.is_empty()
        }
        Err(e) => {
            errors.push(format!("Failed to parse as container YAML: {}", e));
            false
        }
    };

    Ok(ValidationResult {
        file: file.display().to_string(),
        valid,
        errors,
        warnings,
        compatibility_issues,
        metadata,
    })
}

fn print_validation_result(result: &ValidationResult, verbose: bool) {
    if result.valid {
        println!("✓ Valid: {}", result.file);

        if verbose {
            println!("  Test Results: {}", result.metadata.test_results_count);
            println!("  Total Steps: {}", result.metadata.total_steps);
            println!("  Has Title: {}", result.metadata.has_title);
            println!("  Has Project: {}", result.metadata.has_project);
            println!("  Has Test Date: {}", result.metadata.has_test_date);
            println!("  Has Metadata: {}", result.metadata.has_metadata);
        }
    } else {
        println!("✗ Invalid: {}", result.file);
    }

    if !result.errors.is_empty() {
        println!("\nErrors:");
        for error in &result.errors {
            println!("  ✗ {}", error);
        }
    }

    if verbose && !result.warnings.is_empty() {
        println!("\nWarnings:");
        for warning in &result.warnings {
            println!("  ⚠ {}", warning);
        }
    }

    if verbose && !result.compatibility_issues.is_empty() {
        println!("\nCompatibility Issues:");
        for issue in &result.compatibility_issues {
            println!("  ⚠ {}", issue);
        }
    }
}

fn validate_batch(
    directory: &Path,
    pattern: Option<&str>,
    recursive: bool,
) -> Result<CompatibilityReport> {
    let pattern = pattern.unwrap_or("*container*.y*ml");

    let mut results = Vec::new();
    let mut valid_files = 0;
    let mut invalid_files = 0;

    let files = find_container_files(directory, pattern, recursive)?;

    println!("Found {} container YAML files", files.len());
    println!();

    for file in files {
        let result = validate_container_file(&file, false)?;

        if result.valid {
            valid_files += 1;
        } else {
            invalid_files += 1;
        }

        print_validation_result(&result, false);
        results.push(result);
        println!();
    }

    // Collect compatibility issues summary
    let mut compatibility_summary = HashMap::new();
    for result in &results {
        for issue in &result.compatibility_issues {
            *compatibility_summary.entry(issue.clone()).or_insert(0) += 1;
        }
    }

    let compatibility_summary: Vec<String> = compatibility_summary
        .into_iter()
        .map(|(issue, count)| format!("{} (occurred {} times)", issue, count))
        .collect();

    Ok(CompatibilityReport {
        total_files: results.len(),
        valid_files,
        invalid_files,
        results,
        compatibility_summary,
    })
}

fn print_batch_results(report: &CompatibilityReport) {
    println!("{}", "=".repeat(70));
    println!("Batch Validation Summary");
    println!("{}", "=".repeat(70));
    println!();
    println!("Total Files: {}", report.total_files);
    println!("Valid Files: {}", report.valid_files);
    println!("Invalid Files: {}", report.invalid_files);

    if !report.compatibility_summary.is_empty() {
        println!();
        println!("Common Compatibility Issues:");
        for issue in &report.compatibility_summary {
            println!("  • {}", issue);
        }
    }
}

fn find_container_files(directory: &Path, pattern: &str, recursive: bool) -> Result<Vec<PathBuf>> {
    let mut files = Vec::new();

    if !directory.exists() {
        return Err(anyhow::anyhow!(
            "Directory not found: {}",
            directory.display()
        ));
    }

    if recursive {
        for entry in walkdir::WalkDir::new(directory)
            .follow_links(true)
            .into_iter()
            .filter_map(|e| e.ok())
        {
            let path = entry.path();
            if path.is_file() && matches_pattern(path, pattern) {
                files.push(path.to_path_buf());
            }
        }
    } else {
        for entry in fs::read_dir(directory)? {
            let entry = entry?;
            let path = entry.path();
            if path.is_file() && matches_pattern(&path, pattern) {
                files.push(path);
            }
        }
    }

    Ok(files)
}

fn matches_pattern(path: &Path, pattern: &str) -> bool {
    let file_name = path.file_name().and_then(|n| n.to_str()).unwrap_or("");

    // Simple pattern matching (supports * wildcard)
    if pattern.contains('*') {
        let parts: Vec<&str> = pattern.split('*').collect();
        let mut pos = 0;

        for (i, part) in parts.iter().enumerate() {
            if i == 0 && !part.is_empty() {
                // First part must match at the start
                if !file_name.starts_with(part) {
                    return false;
                }
                pos += part.len();
            } else if i == parts.len() - 1 && !part.is_empty() {
                // Last part must match at the end
                return file_name.ends_with(part);
            } else if !part.is_empty() {
                // Middle parts must exist in order
                if let Some(idx) = file_name[pos..].find(part) {
                    pos += idx + part.len();
                } else {
                    return false;
                }
            }
        }
        true
    } else {
        file_name == pattern
    }
}

fn test_verifier_scenarios(scenarios_dir: &Path, generate: bool) -> Result<()> {
    println!("Testing verifier scenarios compatibility");
    println!("Scenarios directory: {}", scenarios_dir.display());
    println!();

    if !scenarios_dir.exists() {
        return Err(anyhow::anyhow!(
            "Scenarios directory not found: {}",
            scenarios_dir.display()
        ));
    }

    // Find all test case YAML files
    let test_cases = find_test_case_files(scenarios_dir)?;

    println!("Found {} test case files", test_cases.len());
    println!();

    // For each test case, check if there's a corresponding execution log and verification result
    for test_case in test_cases {
        println!("Testing: {}", test_case.display());

        // Expected paths for this test case
        let test_case_id = test_case
            .file_stem()
            .and_then(|s| s.to_str())
            .unwrap_or("unknown");

        let parent_dir = test_case.parent().unwrap();
        let execution_log = parent_dir.join(format!("{}_execution_log.json", test_case_id));
        let verification_result = parent_dir.join(format!("{}_result.yaml", test_case_id));

        if execution_log.exists() {
            println!("  ✓ Found execution log: {}", execution_log.display());
        } else {
            println!("  ⚠ Missing execution log: {}", execution_log.display());
        }

        if verification_result.exists() {
            println!(
                "  ✓ Found verification result: {}",
                verification_result.display()
            );

            // Validate the result file
            let result = validate_container_file(&verification_result, false)?;
            if result.valid {
                println!("    ✓ Valid container YAML");
            } else {
                println!("    ✗ Invalid container YAML");
                for error in &result.errors {
                    println!("      ✗ {}", error);
                }
            }
        } else {
            println!(
                "  ⚠ Missing verification result: {}",
                verification_result.display()
            );

            if generate {
                println!("    → Would generate using verifier (not implemented yet)");
            }
        }

        println!();
    }

    Ok(())
}

fn find_test_case_files(directory: &Path) -> Result<Vec<PathBuf>> {
    let mut files = Vec::new();

    for entry in walkdir::WalkDir::new(directory)
        .follow_links(true)
        .into_iter()
        .filter_map(|e| e.ok())
    {
        let path = entry.path();
        if path.is_file() {
            let file_name = path.file_name().and_then(|n| n.to_str()).unwrap_or("");

            // Match test case YAML files (but not execution logs or results)
            if (file_name.ends_with(".yml") || file_name.ends_with(".yaml"))
                && !file_name.contains("_execution_log")
                && !file_name.contains("_result")
                && !file_name.contains("container")
            {
                files.push(path.to_path_buf());
            }
        }
    }

    Ok(files)
}

fn generate_compatibility_report(
    directory: &Path,
    output: Option<&Path>,
    format: &str,
) -> Result<()> {
    let report = validate_batch(directory, None, true)?;

    let report_content = match format {
        "markdown" => generate_markdown_report(&report)?,
        "text" => generate_text_report(&report)?,
        _ => return Err(anyhow::anyhow!("Unknown report format: {}", format)),
    };

    if let Some(output_path) = output {
        fs::write(output_path, report_content)?;
        println!("Report written to: {}", output_path.display());
    } else {
        println!("{}", report_content);
    }

    Ok(())
}

fn generate_markdown_report(report: &CompatibilityReport) -> Result<String> {
    let mut md = String::new();

    md.push_str("# Test Plan Documentation Generator Compatibility Report\n\n");

    md.push_str("## Summary\n\n");
    md.push_str(&format!("- **Total Files**: {}\n", report.total_files));
    md.push_str(&format!("- **Valid Files**: {}\n", report.valid_files));
    md.push_str(&format!("- **Invalid Files**: {}\n", report.invalid_files));
    md.push_str(&format!(
        "- **Success Rate**: {:.1}%\n",
        (report.valid_files as f64 / report.total_files as f64) * 100.0
    ));
    md.push('\n');

    if !report.compatibility_summary.is_empty() {
        md.push_str("## Compatibility Issues Summary\n\n");
        for issue in &report.compatibility_summary {
            md.push_str(&format!("- {}\n", issue));
        }
        md.push('\n');
    }

    md.push_str("## Detailed Results\n\n");
    for result in &report.results {
        md.push_str(&format!("### {}\n\n", result.file));

        if result.valid {
            md.push_str("**Status**: ✓ Valid\n\n");
        } else {
            md.push_str("**Status**: ✗ Invalid\n\n");
        }

        md.push_str(&format!(
            "- Test Results: {}\n",
            result.metadata.test_results_count
        ));
        md.push_str(&format!("- Total Steps: {}\n", result.metadata.total_steps));
        md.push_str(&format!(
            "- Has Required Fields: Title={}, Project={}, Test Date={}\n",
            result.metadata.has_title, result.metadata.has_project, result.metadata.has_test_date
        ));
        md.push('\n');

        if !result.errors.is_empty() {
            md.push_str("**Errors**:\n");
            for error in &result.errors {
                md.push_str(&format!("- {}\n", error));
            }
            md.push('\n');
        }

        if !result.warnings.is_empty() {
            md.push_str("**Warnings**:\n");
            for warning in &result.warnings {
                md.push_str(&format!("- {}\n", warning));
            }
            md.push('\n');
        }

        if !result.compatibility_issues.is_empty() {
            md.push_str("**Compatibility Issues**:\n");
            for issue in &result.compatibility_issues {
                md.push_str(&format!("- {}\n", issue));
            }
            md.push('\n');
        }
    }

    md.push_str("## Schema Compatibility\n\n");
    md.push_str("The container YAML format expected by test-plan-doc-gen requires:\n\n");
    md.push_str("### Required Fields\n\n");
    md.push_str("- `test_results`: Array of test result objects (must not be empty)\n");
    md.push_str("  - Each test result must have:\n");
    md.push_str("    - `test_case_id`: Non-empty string\n");
    md.push_str("    - `sequences`: Array of sequence objects (must not be empty)\n");
    md.push_str("    - `total_steps`: Number\n");
    md.push_str("    - `passed_steps`: Number\n");
    md.push_str("    - `failed_steps`: Number\n");
    md.push_str("    - `not_executed_steps`: Number\n");
    md.push_str("    - `overall_pass`: Boolean\n");
    md.push('\n');

    md.push_str("### Recommended Fields\n\n");
    md.push_str("- `title`: Report title\n");
    md.push_str("- `project`: Project name\n");
    md.push_str("- `test_date`: ISO 8601 formatted date\n");
    md.push_str("- `metadata`: Execution metadata object\n");
    md.push('\n');

    md.push_str("### Step Result Variants\n\n");
    md.push_str("Step results must use one of these enum variants:\n\n");
    md.push_str("- `Pass`: Successful step execution\n");
    md.push_str("- `Fail`: Failed step execution (should include `expected`, `actual_result`, `actual_output`, `reason`)\n");
    md.push_str("- `NotExecuted`: Step was not executed\n");
    md.push('\n');

    Ok(md)
}

fn generate_text_report(report: &CompatibilityReport) -> Result<String> {
    let mut text = String::new();

    text.push_str(&"=".repeat(70));
    text.push('\n');
    text.push_str("Test Plan Documentation Generator Compatibility Report\n");
    text.push_str(&"=".repeat(70));
    text.push_str("\n\n");

    text.push_str("SUMMARY\n");
    text.push_str(&"-".repeat(70));
    text.push('\n');
    text.push_str(&format!("Total Files:   {}\n", report.total_files));
    text.push_str(&format!("Valid Files:   {}\n", report.valid_files));
    text.push_str(&format!("Invalid Files: {}\n", report.invalid_files));
    text.push_str(&format!(
        "Success Rate:  {:.1}%\n",
        (report.valid_files as f64 / report.total_files as f64) * 100.0
    ));
    text.push('\n');

    if !report.compatibility_summary.is_empty() {
        text.push_str("COMPATIBILITY ISSUES SUMMARY\n");
        text.push_str(&"-".repeat(70));
        text.push('\n');
        for issue in &report.compatibility_summary {
            text.push_str(&format!("• {}\n", issue));
        }
        text.push('\n');
    }

    text.push_str("DETAILED RESULTS\n");
    text.push_str(&"-".repeat(70));
    text.push_str("\n\n");

    for result in &report.results {
        if result.valid {
            text.push_str(&format!("✓ {}\n", result.file));
        } else {
            text.push_str(&format!("✗ {}\n", result.file));
        }

        text.push_str(&format!(
            "  Test Results: {}, Total Steps: {}\n",
            result.metadata.test_results_count, result.metadata.total_steps
        ));

        if !result.errors.is_empty() {
            text.push_str("  Errors:\n");
            for error in &result.errors {
                text.push_str(&format!("    ✗ {}\n", error));
            }
        }

        text.push('\n');
    }

    Ok(text)
}
